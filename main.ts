// set up hero animations
game.onUpdate(function () {
    if (Stephanie.vx < 0) {
        StephanieFacingleft = true
    } else if (Stephanie.vx > 0) {
        StephanieFacingleft = false
    }
    if (Stephanie.isHittingTile(CollisionDirection.Top)) {
        Stephanie.vy = 0
    }
    if (controller.down.isPressed()) {
        if (StephanieFacingleft) {
            animation.setAction(Stephanie, ActionKind.CrouchLeft)
        } else {
            animation.setAction(Stephanie, ActionKind.CrouchRight)
        }
    } else if (Stephanie.vy < 20 && !(hero.isHittingTile(CollisionDirection.Bottom))) {
        if (StephanieFacingleft) {
            animation.setAction(Stephanie, ActionKind.JumpingLeft)
        } else {
            animation.setAction(Stephanie, ActionKind.JumpingRight)
        }
    } else if (Stephanie.vx < 0) {
        animation.setAction(Stephanie, ActionKind.RunningLeft)
    } else if (Stephanie.vx > 0) {
        animation.setAction(hero, ActionKind.RunningRight)
    } else {
        if (StephanieFacingleft) {
            animation.setAction(hero, ActionKind.IdleLeft)
        } else {
            animation.setAction(Stephanie, ActionKind.IdleRight)
        }
    }
})
// bumper movement
game.onUpdate(function () {
    for (let value8 of sprites.allOfKind(SpriteKind.Bumper)) {
        if (value8.isHittingTile(CollisionDirection.Left)) {
            value8.vx = randint(30, 60)
        } else if (value8.isHittingTile(CollisionDirection.Right)) {
            value8.vx = randint(-60, -30)
        }
    }
})
// Flier movement
game.onUpdate(function () {
    for (let value9 of sprites.allOfKind(SpriteKind.Flier)) {
        if (Math.abs(value9.x - hero.x) < 60) {
            if (value9.x - hero.x < -5) {
                value9.vx = 25
            } else if (value9.x - hero.x > 5) {
                value9.vx = -25
            }
            if (value9.y - hero.y < -5) {
                value9.vy = 25
            } else if (value9.y - hero.y > 5) {
                value9.vy = -25
            }
            animation.setAction(value9, ActionKind.Flying)
        } else {
            value9.vy = -20
            value9.vx = 0
            animation.setAction(value9, ActionKind.Idle)
        }
    }
})
